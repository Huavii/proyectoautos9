        private void configurarTabla() {
        String[] columnas = {
            "Código", "Marca", "Modelo", "Color","Año", "Tipo", "Precio Base", "Estado"};
        DefaultTableModel modelo = new DefaultTableModel(null, columnas);
        tbLibro.setModel(modelo);
    }



    private void actualizarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) tbLibro.getModel();
        modelo.setRowCount(0);  // limpia tabla

        for (int i = 0; i < servicioVehiculos.getContador(); i++) {
            Vehiculo v = servicioVehiculos.getVehiculo(i);

            Object[] fila = {
                v.getCodigo(),
                v.getMarca(),
                v.getModelo(),
                v.getColor(),
                v.getAño(),
                v.getTipo(),
                v.getPrecioBase(),
                v.getEstado()
            };

            modelo.addRow(fila);
        }
    }


