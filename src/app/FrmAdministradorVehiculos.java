    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
   
        Vehiculo v = new Vehiculo(
            txtCodigo.getText(),
            txtMarca.getText(),
            txtModelo.getText(),
            txtColor.getText(),
            txtAñofabricacion.getText(),
            txtTipo.getText(),
            Double.parseDouble(txtPreciob.getText()),
            txtEstado.getText()
        );

        servicioVehiculos.agregar(v);
        actualizarTabla();

    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        
        String codigo = txtCodigo.getText();
        Vehiculo v = servicioVehiculos.buscarPorCodigo(codigo);

        if (v != null) {
            v.setMarca(txtMarca.getText());
            v.setModelo(txtModelo.getText());
            v.setColor(txtColor.getText());
            v.setAño(txtAñofabricacion.getText());
            v.setTipo(txtTipo.getText());
            v.setPrecioBase(Double.parseDouble(txtPreciob.getText()));
            v.setEstado(txtEstado.getText());
        }

        actualizarTabla();
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        String codigo = txtCodigo.getText();
        servicioVehiculos.eliminarPorCodigo(codigo);
        actualizarTabla();
    
    }//GEN-LAST:event_btnEliminarActionPerformed

