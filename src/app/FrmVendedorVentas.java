    private void configurarTablas() {

        String[] cabCoti = { "N° Coti", "DNI Cliente", "Vehículo", "Total" };
        tbCotizaciones.setModel(new DefaultTableModel(null, cabCoti));


        String[] cabVentas = {
            "N° Cotización", "DNI Cliente", "Vehículo",
            "Total a pagar", "Vendedor", "Tipo facturación", "Fecha y hora"
        };
        tbVentas.setModel(new DefaultTableModel(null, cabVentas));
    }

    private void actualizarTablas() {
        actualizarTablaCotizaciones();
        actualizarTablaVentas();
    }

    private void actualizarTablaCotizaciones() {
        DefaultTableModel modelo = (DefaultTableModel) tbCotizaciones.getModel();
        modelo.setRowCount(0);

        for (int i = 0; i < servicioCotizaciones.getContador(); i++) {
            Cotizacion c = servicioCotizaciones.getCotizacion(i);

            Cliente cli = c.getCliente();
            Vehiculo v  = c.getVehiculo();

            String dniCli  = cli.getDni();
            String descVeh = v.getCodigo() + " - " + v.getMarca() + " " + v.getModelo();
            double total   = c.getPrecioFinal();   

            Object[] fila = {
                c.getNumero(),  
                dniCli,
                descVeh,
                total
            };
            modelo.addRow(fila);
        }
    }

    private void actualizarTablaVentas() {
        DefaultTableModel modelo = (DefaultTableModel) tbVentas.getModel();
        modelo.setRowCount(0);

        Venta[] arr = servicioVentas.getVentas();

        for (int i = 0; i < servicioVentas.getContador(); i++) {
            Venta vta = arr[i];
            Cotizacion c = vta.getCotizacion();
            Cliente cli  = c.getCliente();
            Vehiculo veh = c.getVehiculo();

            String dniCli  = cli.getDni();
            String descVeh = veh.getCodigo() + " - " + veh.getMarca() + " " + veh.getModelo();

            Object[] fila = {
                vta.getNumeroVenta(),      
                dniCli,
                descVeh,
                vta.getTotal(),
                vta.getVendedor(),
                vta.getTipoFacturacion(),
                vta.getFecha()
            };
            modelo.addRow(fila);
        }
    }
    
