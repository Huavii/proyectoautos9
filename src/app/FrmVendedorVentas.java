    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String numCoti  = txtNumeroCotizacion.getText().trim();
        String tipoFact = txtTipoFacturacion.getText().trim(); 

        if (numCoti.isEmpty() || tipoFact.isEmpty()) {
            return; 
        }

    
        Cotizacion c = servicioCotizaciones.buscarPorNumero(numCoti);
        if (c == null) {
            return; 
        }


        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy HH:mm");
        String fecha = sdf.format(new Date());


        Venta vta = new Venta(
            numCoti,      
            c,
            fecha,
            tipoFact,
            vendedorActual   
        );

        servicioVentas.agregar(vta);
        actualizarTablaVentas();
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnEliminar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminar1ActionPerformed
        String numero = txtNumeroCotizacion.getText().trim();
        if (numero.isEmpty()) {
            return;
        }

        servicioVentas.eliminarPorNumero(numero);
        actualizarTablaVentas();
    }//GEN-LAST:event_btnEliminar1ActionPerformed

